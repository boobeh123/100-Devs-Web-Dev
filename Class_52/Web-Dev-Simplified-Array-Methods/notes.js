/**************************************************************
* some()
***************************************************************/
/*
*/

let hasCheapItems = items.some((element) => {
    return element.price <= 75
})

console.log(hasCheapItems);
/**************************************************************
* some()
***************************************************************/




/**************************************************************
* every()
***************************************************************/
/*
*/

let hasExpensiveItems = items.every((element) => {
    return element.price <= 1000
})

console.log(hasExpensiveItems);
/**************************************************************
* every()
***************************************************************/




/**************************************************************
* reduce()
***************************************************************/
/*
*/

let total = items.reduce((currentTotal, item) => {
    return item.price + currentTotal
}, 0)
console.log(total)
